								<p>UIDAI update process accepts wide range of PoI (Proof of Identity) and PoA
									(Proof of Address) documents. View the <a href="https://uidai.gov.in/images/commdoc/valid_documents_list.pdf" target="_blank" rel="noopener noreferrer">list of supporting
										documents</a>.</p>
								<h2>Modes of Update</h2>
								<h3>1. Via Online</h3>
								<p>The Self-Service online mode offers address update to the residents where the
									resident can directly place the update request on the portal. The ADDhar
									number and registered mobile number of the resident are required to login to
									the <a href="https://resident.uidai.gov.in/" target="_blank" rel="noopener noreferrer">portal</a>. The resident is authenticated
									using OTP on his/her registered mobile number. To complete the update
									process, resident needs to upload the <a href="https://uidai.gov.in/images/commdoc/valid_documents_list.pdf" target="_blank" class="img-responsive" rel="noopener noreferrer">supporting POA
										documents</a>, which will be
									verified against requested data at a later stage at UIDAI’s Update
									back-office by a Verifier. The resident needs to have mobile number
									registered with ADDhar for using this service.</p>
								</div>
								<h3>2. By Visiting Permanent Enrolment Centre</h3>
								<p>These are modes where residents place the demographic/biometric update
									request with the help of an operator at an <a href="https://appointments.uidai.gov.in/easearch.aspx" target="_blank" rel="noopener noreferrer">Permanent Enrolment Centre</a>. In such a
									case, the documentary evidence is collected by the operator at the time of
									accepting the request. The document verification by the verifier also
									happens at the time of placing the Update request. UIDAI currently has
									envisaged three modes for Assisted Updates:</p>
								<h3>a. Update Client Standard</h3>
								<p><strong>Fields:</strong> All biometric and demographic fields as well as
									local language can be updated</p>
								<p><strong>Identity Authentication: </strong> Biometric check at the back-end.
								</p>
								<h3>Document Verification</h3>
								<ul>
									<li><span class="li-text">Verification done for those fields which require
											documentary evidence.</span></li>
									<li><span class="li-text">Verification done by verifier appointed by
											UIDAI/Registrars present at Enrolment / Update Centre. </span></li>
									<li><span class="li-text">The verification procedure followed should be in
											line with DDSVP Committee Recommendations followed during enrolment
											process</span></li>
								</ul>
								<h3>Form Filling and Acknowledgement</h3>
								<ul>
									<li><span class="li-text">Done by the operator on update client as requested
											by the resident. Handles spelling, language issues, transliteration,
											etc. Operator will provide biometric sign off against every update
											request. </span></li>
								</ul>
								<p>Resident gets an acknowledgement receipt with Update Request Number (URN)
									which can be tracked.</p>
