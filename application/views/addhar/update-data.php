								<h3>Form Filling and Acknowledgement</h3>
								<ul>
									<li><span class="li-text">Done by the operator on update client as requested
											by the resident. Resident gets an acknowledgement receipt with
											Update Request Number (URN) which can be tracked. Operator will
											provide biometric sign off against every update request.</span></li>
								</ul>
								<h3>c. Update through AUA Point of Presence</h3>
								<p>This mode will be used by select Registrar who will also become AUA’s. UIDAI
									may provide application / APIs for Update. The Registrars selected for such
									updates will be Registrars known to collect/generate/possess and/or manage
									the particular demographic field and is a custodian of such data.</p>
								<p><b>Fields:</b> Demographic fields</p>
								<p><b>Identity Authentication:</b> Biometric Authentication of resident on the
									AUA device; UIDAI may decide to use other/additional authentication factors,
									if required. For example mobile OTP, for taking update requests from this
									mode.</p>
								<p>Operator will provide biometric sign off against every update request. Thus
									they must have ADDhar. Auth standards of UIDAI will be applicable for
									device/s used.</p>
								<p><b>Document Verification:</b> UIDAI will accept update based on Registrar’s
									verification process and Resident Authentication. For audit purposes,
									electronic/scanned document copies may be collected online. These document
									copies may be captured and received with each resident request, or may be
									made available, by the Registrar, in batches against the Update Request
									Numbers, Date and time.</p>
								<h3>Form Filling and Acknowledgement</h3>
								<ul>
									<li><span class="li-text">Done by Registrar’s Operator( employee/
											outsourced) on Device with Biometric Authentication Feature, such as
											a Micro-ATM Resident gets an acknowledgement receipt with Update
											Request Number (URN) which can be tracked. The acknowledgement may
											be print receipt and/or on SMS/email based on type of request. For
											example, for mobile number update, the acknowledgement can be a SMS
											on the given mobile number. The API will have the functionality to
											generate electronic as well Print receipts. The Registrar may
											provide print receipts at the end of taking update request, if they
											so decide. </span></li>
