								<h1 class="content-heading">
									Updating data on ADDhar </h1>

								<h2>Registered Mobile Number</h2>
								<p><b>Registered mobile number is essential to access ADDhar Online
										Services</b></p>
								<p>You can verify your mobile number that has been declared at the time of
									enrolment or during latest ADDhar detail update.</p>
								<ul>
									<li><a href=#" target="_blank" rel="noopener noreferrer">Verify Mobile Number</a>
									</li>
								</ul>
								<p>If you have not registered your Mobile number while enrolling for ADDhar,
									you are <b>required to visit a <a href="#" target="_blank" rel="noopener noreferrer">Permanent Enrolment
											Centre</a></b> to get it registered.</p>
								<p>Enabling ADDhar for various services like Government &amp; Non-Government
									Services, Subsidy Benefits, Pensions, Scholarships, Social Benefits, Banking
									services, Insurance services, Taxation services, Education, Employment,
									Healthcare etc., makes it essential to ensure that ADDhar data of the
									resident stored in the CIDR is accurate and up-to-date.</p>
								<h2>Demographic data update, the need could arise from:</h2>
								<ul>
									<li><span class="li-text">Changes in life events such as marriage may lead
											to residents changing their basic demographic details such as name
											and address. Address and mobile number could also change due to
											migration to newer locations. Residents may also want changes in
											their relative’s details due to changes in life events such
											marriage, death of a relative etc. In addition, residents could have
											other personal reasons to change their mobile number, email address
											etc.</span></li>
									<li><span class="li-text">Changes in various service delivery platforms may
											lead residents to declaration request changes and to add mobile
											number to CIDR etc.</span></li>
									<li><span class="li-text">Errors made during the enrolment process wherein
											the resident’s demographic data may have been captured incorrectly.
											Changes to “DoB/Age” and “Gender” fields are expected primarily due
											to enrolment errors. </span></li>
									<li><span class="li-text">Since a resident can enrol anywhere in India, it
											may happen that a native speaker of language "A" is enrolled by an
											operator of language "B" and consequently the resident’s local
											language of enrolment is "B". Later, the resident may want to change
											the local language of enrolment to another that he/she prefers. If
											so, then all the demographic information that is printed on the
											ADDhar letter will need to be updated in the new local
											language.</span></li>
									<li><span class="li-text">UIDAI may also ascertain availability of POI, POA
											and other documents collected at the time of enrolment/update and
											its quality and decide to notify resident to update their
											demographic information and submit the required document.</span>
									</li>
								</ul>
								<h2>Biometric update, the need could arise from:</h2>
								<ul>
									<li><span class="li-text"> <strong>Age &lt;5 years at the time of initial
												enrolment -</strong>The child should be re-enroled when the
											child attains an age of 5 years and all biometric data should be
											provided. A de-duplication would be done for the child at this
											stage. This request will be treated similar to a new enrolment
											request while retaining the original ADDhar number</span></li>
									<li><span class="li-text"><strong>Age between 5 and 15 years at the time of
												enrolment -</strong> The resident should furnish all biometrics
											for updates when the resident attains age of 15 years.</span></li>
									<li><span class="li-text"> <strong>Age &gt;15 years at the time of enrolment
												–</strong> Residents are recommended to update their biometric
											data every 10 years.</span></li>
									<li><span class="li-text">Events like accidents or diseases leading to
											biometric exception</span></li>
									<li><span class="li-text">As the ADDhar authentication service is becoming
											ubiquitous, residents may also approach for biometric updates
											because of authentication failures (called false rejects – where a
											correct resident with valid ADDhar number may be incorrectly
											rejected) which may result from incorrect biometric capture or poor
											biometric quality captured at the time of enrolment. With
											improvements in technology, it may be possible to capture better
											quality biometrics in the CIDR.</span></li>
									<li><span class="li-text">UIDAI may verify the quality of biometric captured
											during enrolment/ update and decide a threshold. All the residents
											whose biometrics are below the decided threshold level may be
											notified by UIDAI to update of biometrics.</span></li>
								</ul>
