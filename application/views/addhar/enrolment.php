												documents</a>. Common proofs of identity and address are
											election photo ID card, Ration card, passport and driving
											license.</span></li>
									<li><span class="li-text"> Photo ID cards like PAN card and Govt ID cards
											are permissible for identity proof. Address proof documents also
											include water - electricity - Landline telephone bills for the last
